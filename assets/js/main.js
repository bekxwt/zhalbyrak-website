  const SUPPORTED = ["ru", "ky"];
  const STORAGE_KEY = "zhalbyrak_lang";

  function normalizeLang(lang) {
    if (!lang) return "ru";
    const l = String(lang).toLowerCase();
    if (l.startsWith("ky")) return "ky";
    if (l.startsWith("ru")) return "ru";
    return "ru";
  }

  function detectLang() {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved && SUPPORTED.includes(saved)) return saved;

    const nav = (navigator.languages && navigator.languages[0]) || navigator.language || "ru";
    return normalizeLang(nav);
  }

  function t(lang) {
    return I18N[lang] || I18N.ru;
  }

  function applyText(lang) {
    const dict = t(lang);

    document.documentElement.setAttribute("lang", lang);

    // Generic: elements with data-i18n="path.to.key"
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const path = el.getAttribute("data-i18n");
      if (!path) return;

      const value = path.split(".").reduce((acc, k) => (acc ? acc[k] : undefined), dict);
      if (typeof value === "string") el.textContent = value;
    });

    // HTML content support: data-i18n-html
    document.querySelectorAll("[data-i18n-html]").forEach((el) => {
      const path = el.getAttribute("data-i18n-html");
      if (!path) return;

      const value = path.split(".").reduce((acc, k) => (acc ? acc[k] : undefined), dict);
      if (typeof value === "string") el.innerHTML = value;
    });

    // Features list
    const featureCards = document.querySelectorAll("[data-feature-card]");
    if (featureCards.length) {
      const items = dict.features.items || [];
      featureCards.forEach((card, idx) => {
        const item = items[idx];
        if (!item) return;
        const [icon, title, text] = item;

        const iconEl = card.querySelector("[data-feature-icon]");
        const titleEl = card.querySelector("[data-feature-title]");
        const textEl = card.querySelector("[data-feature-text]");
        if (iconEl) iconEl.textContent = icon;
        if (titleEl) titleEl.textContent = title;
        if (textEl) textEl.textContent = text;
      });
    }

    // How steps
    const steps = document.querySelectorAll("[data-step]");
    if (steps.length) {
      const items = dict.how.steps || [];
      steps.forEach((row, idx) => {
        const item = items[idx];
        if (!item) return;
        const [num, title, text] = item;

        const numEl = row.querySelector("[data-step-num]");
        const titleEl = row.querySelector("[data-step-title]");
        const textEl = row.querySelector("[data-step-text]");
        if (numEl) numEl.textContent = num;
        if (titleEl) titleEl.textContent = title;
        if (textEl) textEl.textContent = text;
      });
    }

    // Update switcher labels
    document.querySelectorAll("[data-lang-label]").forEach((el) => {
      el.textContent = dict.langNameShort;
    });
    document.querySelectorAll("[data-lang-name-long]").forEach((el) => {
      el.textContent = dict.langNameLong;
    });

    // Brand tag under logo
    document.querySelectorAll("[data-brand-tag]").forEach((el) => {
      el.textContent = dict.brandTag;
    });

    // Footer rights
    document.querySelectorAll("[data-footer-rights]").forEach((el) => {
      el.textContent = dict.footer.rights;
    });

    // Page title fragments where needed
    const titleEl = document.querySelector("[data-page-title]");
    if (titleEl) titleEl.textContent = dict[titleEl.getAttribute("data-page-title")]?.title || titleEl.textContent;

    // Support label
    document.querySelectorAll("[data-support-label]").forEach((el) => {
      el.textContent = dict.download.support;
    });
  }

  function setLang(lang) {
    const normalized = normalizeLang(lang);
    localStorage.setItem(STORAGE_KEY, normalized);
    applyText(normalized);

    document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
      const v = btn.getAttribute("data-lang-btn");
      btn.setAttribute("aria-pressed", v === normalized ? "true" : "false");
    });
  }

  // Year
  const year = document.getElementById("year");
  if (year) year.textContent = String(new Date().getFullYear());

  // Mobile nav toggle (existing)
  const navToggle = document.getElementById("navToggle");
  const mobileNav = document.getElementById("mobileNav");
  function setOpen(open) {
    if (!navToggle || !mobileNav) return;
    navToggle.setAttribute("aria-expanded", open ? "true" : "false");
    mobileNav.hidden = !open;
    if (!open) mobileNav.scrollTop = 0;
  }
  if (navToggle && mobileNav) {
    navToggle.addEventListener("click", () => {
      const expanded = navToggle.getAttribute("aria-expanded") === "true";
      setOpen(!expanded);
    });
    mobileNav.addEventListener("click", (e) => {
      const target = e.target;
      if (target && target.tagName === "A") setOpen(false);
    });
    window.addEventListener("resize", () => {
      if (window.innerWidth > 980) setOpen(false);
    });
  }

  // Language buttons
  document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
    btn.addEventListener("click", () => {
      const v = btn.getAttribute("data-lang-btn");
      if (v) setLang(v);
    });
  });

  // Init language
  setLang(detectLang());
